export function getProductsByCategory(category: string, subcategory?: string): Product[] {
  console.log('Filtering products for category:', category, 'subcategory:', subcategory);
  
  const filtered = sampleProducts.filter(product => {
    if (subcategory) {
      // Filter by both category and subcategory
      const matches = product.category === category && product.subcategory === subcategory;
      console.log(`Product ${product.name}: category=${product.category}, subcategory=${product.subcategory}, matches=${matches}`);
      return matches;
    }
    // Filter by category only
    const matches = product.category === category;
    console.log(`Product ${product.name}: category=${product.category}, matches=${matches}`);
    return matches;
  });
  
  console.log('Filtered products:', filtered.length, filtered.map(p => p.name));
  return filtered;
}

export function getProductsBySize(category: string, subcategory?: string, size?: string): Product[] {
  console.log('Filtering products by size:', { category, subcategory, size });
  
  // Start with all products
  let filtered = sampleProducts;
  
  // First filter by category (MANDATORY)
  filtered = filtered.filter(product => product.category === category);
  console.log(`After category filter (${category}):`, filtered.length, 'products');
  
  // Then filter by subcategory if specified
  if (subcategory) {
    filtered = filtered.filter(product => product.subcategory === subcategory);
    console.log(`After subcategory filter (${subcategory}):`, filtered.length, 'products');
  }
  
  // Finally filter by size if specified
  if (size) {
    filtered = filtered.filter(product => 
      product.availableSizes.includes(size) || product.sizes.includes(size)
    );
    console.log(`After size filter (${size}):`, filtered.length, 'products');
  }
  
  console.log('Final filtered products:', filtered.map(p => ({ 
    name: p.name, 
    category: p.category, 
    subcategory: p.subcategory, 
    sizes: p.sizes 
  })));
  
  return filtered;
}

export function getRecentProducts(): Product[] {
  const recentProducts = sampleProducts.filter(product => product.isNew);
  console.log('Recent products found:', recentProducts.length);
  return recentProducts;
}

export function searchProducts(term: string): Product[] {
  const searchTerm = term.toLowerCase();
  return sampleProducts.filter(product => 
    product.name.toLowerCase().includes(searchTerm) ||
    product.category.toLowerCase().includes(searchTerm) ||
    product.subcategory.toLowerCase().includes(searchTerm) ||
    product.description?.toLowerCase().includes(searchTerm)
  );
}